const raqam = 7.7

// Math.round() - sonning eng yaqin butun qismiga qarab yaxlitlaydi.
console.log(Math.round(raqam))

// Math.floor() - sonning yaqin kichik butun qismiga qarab yaxlitlaydi.
console.log(Math.floor(raqam))

// Math.ceil() - sonning eng yaqin katta butun qismiga qarab yaxlitlaydi.
console.log(Math.ceil(raqam))

// Math.trunc() - sonni faqat butun qismini qaytaradi.
console.log(Math.trunc(raqam))

// Math.random() - 0 va 1 oralig'ida ixtiyoriy raqamni qaytaradi.
console.log(Math.random())

// Math.random() da misol uchun 0 va 10 orasidagi son chiqarmoqchi bo'lsak Math.random() * 10 qilamiz.
